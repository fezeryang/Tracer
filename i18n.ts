export const getInitialLanguage = (): Language => {
  if (typeof window === 'undefined') return 'zh';
  const saved = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return saved === 'en' ? 'en' : 'zh';
};

const resolvePath = (obj: MessageTree, path: string): string | undefined => {
  return path.split('.').reduce<string | MessageTree | undefined>((current, key) => {
    if (!current || typeof current === 'string') return current;
    return current[key];
  }, obj) as string | undefined;
};

export const t = (language: Language, key: string, vars?: Record<string, string | number>): string => {
  const raw = resolvePath(messages[language], key) || resolvePath(messages.en, key) || key;
  if (!vars) return raw;

  return Object.entries(vars).reduce((result, [varKey, value]) => {
    return result.replace(new RegExp(`\\{${varKey}\\}`, 'g'), String(value));
  }, raw);
};
